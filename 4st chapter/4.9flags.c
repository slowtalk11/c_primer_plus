#include <stdio.h>
#define PAGES 959
int main(void) {
	printf("%x %X %#x\n",31,31,31);//十六进制小写，大写，0x十六进制; 
	printf("**%d**% d**% d**\n",42,42, -42);//空格表示若符号值为正，显示前导空格，负则显示减号； 
	printf("**%5d**%5.3d**%05d**%05.3d**\n",6,6,6,6);//5.3d代表最小宽度5,3位显示的字符（精度）， 0和精度一起出现会被忽略0。 需要注意记住 
	return 0;
}


